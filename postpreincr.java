public class postpreincr
{
    public static void main(String args[])
    {
    //post increment 
    int p=5,q=5;
    System.out.println(p++);//5->phle assign hoga 5 phir increment hoker 6 ho jayega
    System.out.println(p);//6
    
    //pre increment
    System.out.println(++q);//6->phle increment hoker 6 hoga phir 6 ko assign karega
    System.out.println(q);//6
     }
}
